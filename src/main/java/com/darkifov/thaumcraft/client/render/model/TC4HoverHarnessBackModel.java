    public static void render(PoseStack.Pose pose, VertexConsumer consumer, int light, int overlay) {
        Matrix4f matrix = pose.pose();
        Matrix3f normal = pose.normal();
        // Entity render types consume quads. Repeat the third vertex so every
        // original OBJ triangle remains an independent degenerate quad.
        for (int i = 0; i < TRIANGLES.length; i += 24) {
            emit(i, matrix, normal, consumer, light, overlay);
            emit(i + 8, matrix, normal, consumer, light, overlay);
            emit(i + 16, matrix, normal, consumer, light, overlay);
            emit(i + 16, matrix, normal, consumer, light, overlay);
        }
    }

    private static void emit(int i, Matrix4f matrix, Matrix3f normal,
                             VertexConsumer consumer, int light, int overlay) {
        consumer.vertex(matrix, TRIANGLES[i], TRIANGLES[i + 1], TRIANGLES[i + 2])
                .color(255, 255, 255, 255)
                .uv(TRIANGLES[i + 3], TRIANGLES[i + 4])
                .overlayCoords(overlay)
                .uv2(light)
                .normal(normal, TRIANGLES[i + 5], TRIANGLES[i + 6], TRIANGLES[i + 7])
                .endVertex();
    }
