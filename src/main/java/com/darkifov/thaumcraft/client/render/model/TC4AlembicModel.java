    private static void render(float[] data, PoseStack.Pose pose, VertexConsumer consumer, int light, int overlay) {
        Matrix4f matrix = pose.pose();
        Matrix3f normal = pose.normal();
        // Entity render types use QUADS; preserve each OBJ triangle by
        // repeating its final vertex as a degenerate fourth corner.
        for (int i = 0; i < data.length; i += 24) {
            emit(data, i, matrix, normal, consumer, light, overlay);
            emit(data, i + 8, matrix, normal, consumer, light, overlay);
            emit(data, i + 16, matrix, normal, consumer, light, overlay);
            emit(data, i + 16, matrix, normal, consumer, light, overlay);
        }
    }

    private static void emit(float[] data, int i, Matrix4f matrix, Matrix3f normal,
                             VertexConsumer consumer, int light, int overlay) {
        consumer.vertex(matrix, data[i], data[i + 1], data[i + 2])
                .color(255, 255, 255, 255)
                .uv(data[i + 3], data[i + 4])
                .overlayCoords(overlay)
                .uv2(light)
                .normal(normal, data[i + 5], data[i + 6], data[i + 7])
                .endVertex();
    }
