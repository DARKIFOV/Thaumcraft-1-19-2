    private static void render(float[] data, PoseStack.Pose pose, VertexConsumer consumer, int light, int overlay,
                               int red, int green, int blue, int alpha) {
        Matrix4f matrix = pose.pose();
        Matrix3f normal = pose.normal();
        for (int i = 0; i < data.length; i += 8) {
            consumer.vertex(matrix, data[i], data[i + 1], data[i + 2])
                    .color(red, green, blue, alpha)
                    .uv(data[i + 3], data[i + 4])
                    .overlayCoords(overlay)
                    .uv2(light)
                    .normal(normal, data[i + 5], data[i + 6], data[i + 7])
                    .endVertex();
        }
    }
